/*
 Programa que cuente del 3 al 17 (ambos incluidos) de 2 en 2
 */
package contardos;

/**
 *
 * @author Alumne
 */
public class ContarDos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        for (int i = 3; i <= 17; i+=2) {
            System.out.println(i);
        }
    }
    
}
